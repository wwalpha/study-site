package com.alpha.bean;

public class CalculateBean {
	private int leftnum;
	private int rightnum;
	private int times;

	/**
	 * @return the leftnum
	 */
	public int getLeftnum() {
		return leftnum;
	}

	/**
	 * @param leftnum
	 *            the leftnum to set
	 */
	public void setLeftnum(int leftnum) {
		this.leftnum = leftnum;
	}

	/**
	 * @return the rightnum
	 */
	public int getRightnum() {
		return rightnum;
	}

	/**
	 * @param rightnum
	 *            the rightnum to set
	 */
	public void setRightnum(int rightnum) {
		this.rightnum = rightnum;
	}

	/**
	 * @return the times
	 */
	public int getTimes() {
		return times;
	}

	/**
	 * @param times
	 *            the times to set
	 */
	public void setTimes(int times) {
		this.times = times;
	}

}
